class Calculator:
    def add(self, number_one, number_two):
        return number_one + number_two

    def divide(self, number_one, number_two):
        return number_one / number_two

    def sub(self, number_one, number_two):
        return number_one - number_two

    def multi(self, number_one, number_two):
        return number_one * number_two
